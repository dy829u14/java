package day01;

public class ByteExam {

	public static void main(String[] args) {
		// byte -128 ~ 127
		byte var1 = -128;
		byte var2 = 127;
	//  byte var3 = 128;
		char var3 = 'a';
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
		//String 문자열을 저장 참조타입이다.
		//"" 사용
		String str1 = "김그린";
		System.out.println(str1 + "입니다");

	}

}
